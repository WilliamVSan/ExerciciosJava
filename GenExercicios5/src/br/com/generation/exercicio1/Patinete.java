package br.com.generation.exercicio1;

public class Patinete {
	private String cor;
	private String modelo;
	private String motorizado;
	private String tipoRoda;
	private double preco;
	
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getMotorizado() {
		return motorizado;
	}
	public void setMotorizado(String motorizado) {
		this.motorizado = motorizado;
	}
	public String getTipoRoda() {
		return tipoRoda;
	}
	public void setTipoRoda(String tipoRoda) {
		this.tipoRoda = tipoRoda;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	
	
}

package br.com.generation.exercicio1;

public class ContaBancaria {
	
	private String usuario;
	private String tipo;
	private double quantiDinheiro;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public double getQuantiDinheiro() {
		return quantiDinheiro;
	}
	public void setQuantiDinheiro(double quantiDinheiro) {
		this.quantiDinheiro = quantiDinheiro;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

}
